# ブロックでの評価がtrueになる要素だけを抽出
p [1, 2, 3, 4, 5].find_all {|n| n % 2 == 0}
p [1, 2, 3, 4, 5].select {|n| n % 2 == 0}

# 評価された最初の値を返す
p [1, 2, 3, 4, 5].find {|n| n % 2 == 0}
p [1, 2, 3, 4, 5].detect {|n| n % 2 == 0}

# ブロックで評価した値を返す
p [1, 2, 3, 4, 5].map {|n| n * 2}
p [1, 2, 3, 4, 5].collect {|n| n * 2}

arr1 = [1, 2, 3]
arr2 = [4, 4, 6]
# concatは!が付かない破壊的メソッド
arr1.concat(arr2)
p arr1
